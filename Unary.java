public class Unary {
	public static void main(String args []){
	int A = 10 ;
	int B = 20 ;
System.out.println (A++);
System.out.println (B++);
System.out.println (++A);
System.out.println (++B);
System.out.println (A--);
System.out.println (B--);
System.out.println (--A);
System.out.println (--B);

}	

}