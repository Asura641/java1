public class Relational{
	public static void main (String args []){
		int A = 100;
		int B = 355;
		
		System.out.println(A<B);
		System.out.println(A>B);
		System.out.println(A<=B);
		System.out.println(A>=B);
		System.out.println(A==B);
		System.out.println(A!=B);
		
		System.out.println(A<B && A<200 );
		System.out.println(A<B == A<200 );
		System.out.println(!(A<B) );




		 

}
} 