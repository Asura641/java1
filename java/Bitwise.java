public class Bitwise{
	public static void main (String args []){
		int A = 100;
		int B = 355;
		
		System.out.println(A&B);

		System.out.println(A | B);
		System.out.println(A^B);
		System.out.println(~A );
		System.out.println(5<<1 );
		System.out.println(3 >> 2 );
		



		


		 

}
} 